package Conditions_Loops;

public class Loops {
    public static void main(String[] args) {
        // Syntax of For Loops
        /*
        *- for ( initialization; condition; increment/Decrement){
        *- // body
        *- }
        * */
        for (int num= 1; num <= 9; num+= 1){
            System.out.println(num);
        }
    }
}
